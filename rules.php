			<h6 class="headrules">/action</h6>
			<p>Это описание окружения персонажа, которое остается закрепленным на месте, где была использована команда до тех пор, пока игрок его не удалит. 
			Команда /action является аналогом команды /do, но их различие в том, что после использования команды /action действие вашего персонажа появится 
			не в чате, а рядом с вашим персонажем в виде 3D текста. Данную команду запрещается использовать в качестве рекламной листовки, записи, буклета 
			и т.д. На этот случай имеется функционал доски обьявлений. </p>
			<div class="underrulesblock">
				<div class="urullesblock">
					<h6 class="headrules">Пример неправильной отыгровки:</h6>
					<p>/action В конце улицы стоит полицейский крузер.<br>/action На улице хорошая погода, светит солнце и дует прохладный ветер.<br>/action Шумная толпа надвигалась с юга, они скандировали непонятные лозунги.<br>/action В банк с криками ворвались грабители в масках.</p>
