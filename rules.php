			<p>Старайтесь писать грамотно. Реплики писать с большой буквы. Ставить в конце предложений и действий точку. Действия писать с маленькой буквы. Не используйте знак тире « - » перед каждой репликой, это неправильно.</p>
			<h6 class="headrules">/me and /ame</h6>
			<p>Существует два вида отыгровки действий: "/me" и "/ame". На техническом уровне эти команды отличаются только тем, что "/me" видно в чате, а "/ame" только над головой. В "/me" отыгрывают крупные действия, без которых ситуации не обойтись, а в "/ame" более мелкие действия, которые можно упустить (Например "/ame молча нажал на газ."). Некоторые игроки считают, что главное в этом ремесле отыграть строку, как можно длиннее, но это не так. Конечно, нужно отыгрывать действие побольше, но не стоит описывать самые мелкие детали отыгровки.</p>
			<div class="underrulesblock">
				<div class="urullesblock">
					<h6 class="headrules">Пример неправильной отыгровки:</h6>
					<p>/me подойдя к столу, наклонился к нему корпусом, затем протянул правую руку к чашке, которая стоит на столе, после чего обхватил ее пальцами, выровнял спину и начал пить чай из чашки.</p>
				</div>
				<div class="urullesblock">
					<h6 class="headrules">Пример правильной отыгровки:</h6>
					<p>/me подойдя к столу, взял чашку со стола, после чего начал пить чай.<br>/me взяв букет цветов со стола, опустил тот в вазу с водой.<br>/me зло глянув на Закари сначала дает тому легкую пощечину, а потом и вовсе кидается на него с кулаками.<br>/me вытягивает G19 из пластиковой кобуры и оглядывается по сторонам.</p>
