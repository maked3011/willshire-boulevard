			<h6 class="headrules">/do</h6>
			<p>Для отыгровки от третьего лица нам понадобится команда "/do". Если Вы хотите отыграть свое окружение, что происходит с вами, улицей или другие факторы - используйте эту команду. Не забывайте, что на сервере запрещено отыгрывать за других, но в "/do" можно задать вопрос для того, чтобы понять, что происходит с тем, кто стоит перед вами.</p>
			<div class="underrulesblock">
				<div class="urullesblock">
					<h6 class="headrules">Пример неправильной отыгровки:</h6>
					<p>/do Ричард Роксон мертв. (при этом вы — не Ричард Роксон)<br>/do Присев на стул, откинулась на спинку, при этом закрыла глаза.</p>
