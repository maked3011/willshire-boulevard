				</div>
			</div>
			<h6 class="headrules">/todo</h6>
			<p>Эта команда используется в том случае, когда персонаж говорит, выполняя какое-либо действие. Действия описываются с использованием деепричастного оборота, а также обязательно с маленькой буквы.</p>
			<div class="underrulesblock">
				<div class="urullesblock">
					<h6 class="headrules">Пример неправильной отыгровки:</h6>
					<p>/todo Я знаю.*кивнула головой.<br>/todo Улыбаясь девушке.*привет. (Не нужно путать действие с репликой).<br>/todo Эй, негр, сюда иди!*Мэтью был одет в спортивные штаны и кофту, полностью покрывающую его торс.</p>
				</div>
				<div class="urullesblock">
					<h6 class="headrules">Пример правильной отыгровки:</h6>
