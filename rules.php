			<h6 class="headrules">10. FCK. Правило про FCK</h6>
			<p>Faction Character Kill - полное уничтожение роли путём убийства вашего персонажа, обыгранное той или иной фракцией. Приписывается при запросе исполняющих прямо в игре. FCK можно получить при взаимодействии с той или иной фракцией, при игре с которой вы соглашаетесь на возможное FCK и ряд других нюансов.</p>
			<p>Чтобы убить игрового персонажа путём FCK, необходима весомая причина, полноценное отыгрывание ситуации которая будет запечатлена на видео/скриншотах/скриншот-ситуации где будет видно функционал сервера и взаимодействие с жертвой будет неоспоримым. Примером FCK может стать война банд, в которой подготовленная перестрелка ранее повлечёт за собой смерть персонажей той или иной стороны. Спонтанное убийство любым способом не является поводом для выдачи FCK.</p>
			<p>После смерти вашего персонажа, запрещено регистрировать его родных, друзей, и тому подобных персонажей которые будут продолжать полностью деятельность мертвого персонажа и мстить за него, при таком исходе событий игроку засчитывается RK (Revenge Kill). При нарушении данного правила последует наказание в виде блокировки аккаунта.</p>
			<p>Игрок имеет полноценное право оспорить своё FCK на форуме в разделе жалоб на фракцию которой оно было выдано, фракция будет обязана предоставить все доказательства о ситуации в виде причины, отыгрывания ситуации и прочего. Без наличия весомой причины, доказательств отыгрывания ситуации и последующего отыгрывания персонажа, при спонтанной перестрелке или драке не имеющей корня, при фальсифицированным убийстве на тренинг-сервере, или тому подобных, понятие FCK не приписывается.</p>
			<p>Обязательна 2 стадия или отыгрывание самим погибшим свою смерть независимо от стадии или типа ранения для приписывания понятия FCK.</p>
